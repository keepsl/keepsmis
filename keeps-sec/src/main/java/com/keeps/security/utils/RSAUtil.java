	public static PublicKey getPublicKey(String key) throws Exception {
		byte[] keyBytes = decodeBase64(key);
		X509EncodedKeySpec keySpec = new X509EncodedKeySpec(keyBytes);
		KeyFactory keyFactory = KeyFactory.getInstance(KEY_ALGORITHM);
		PublicKey publicKey = keyFactory.generatePublic(keySpec);
		return publicKey;
	}

	public static byte[] encrypt(PublicKey pubkey, String text) {
		try {
			Cipher rsa;
			rsa = Cipher.getInstance(Algorithm);
			rsa.init(Cipher.ENCRYPT_MODE, pubkey);
			return rsa.doFinal(text.getBytes());
		} catch (Exception e) {
			e.printStackTrace();
		}
		return null;
	}

	public static String decrypt(PrivateKey decryptionKey, byte[] buffer) {
		try {
			Cipher rsa;
			rsa = Cipher.getInstance(Algorithm);
			rsa.init(Cipher.DECRYPT_MODE, decryptionKey);
			byte[] utf8 = rsa.doFinal(buffer);
			return new String(utf8, "UTF8");
		} catch (Exception e) {
			e.printStackTrace();
		}
		return null;
	}
	
    /** 
     * BASE64解密 
     *  
     * @param key 
     * @return 
     * @throws Exception 
     */  
    public static byte[] decodeBase64(String key) throws Exception {
        return org.apache.commons.codec.binary.Base64.decodeBase64(key);  
    }  
  
    /** 
     * BASE64加密 
     *  
     * @param key 
     * @return 
     * @throws Exception 
     */  
    public static String encodeBase64(byte[] key) throws Exception {  
        return org.apache.commons.codec.binary.Base64.encodeBase64String(key);  
    }  

